#include "ft_dict.h"

int	ft_strs_in_strs(char **s1, char **s2)
{
	int	i;
	int	j;
	int	c;

	i = 0;
	while (s2[i])
	{
		j = 0;
		c = 0;
		while (s1[j])
		{
			if (ft_strcmp(s1[j], s2[i]) == 0)
				c = 1;
			j++;
		}
		if (c != 1)
			return (0);
		i++;
	}
	return (1);
}
